CREATE TABLE STORE (
	STORE_ID NUMBER NOT NULL,
	STORE_NAME VARCHAR2(50) NOT NULL,
	STORE_INFO VARCHAR2(200) DEFAULT '' NOT NULL,
	SNSID VARCHAR2(20) NOT NULL,
	SNSTYPE VARCHAR2(20) NOT NULL,
	MOBILE VARCHAR2(11) NOT NULL,
	MOBILE_OPEN_YN VARCHAR2(1) DEFAULT 'N' NOT NULL,
	CONTACT_START_TIME VARCHAR2(4) DEFAULT '0000'  NOT NULL,
	CONTACT_END_TIME VARCHAR2(4) DEFAULT '0000' NOT NULL,
	AUTHENTICATION_MOBILE VARCHAR2(11),
	AUTHENTICATION_YN VARCHAR2(1) DEFAULT 'N' NOT NULL,
	AREA VARCHAR2(20) NOT NULL,
	ZIP_CODE VARCHAR2(5) NOT NULL,
	BASIC_ADDRESS VARCHAR2(50) NOT NULL,
	DTAIL_ADDRESS VARCHAR2(50) NOT NULL,
	NUM_VISITORS NUMBER DEFAULT 0 NOT NULL ,
	NUM_SALES NUMBER DEFAULT 0 NOT NULL,
	NUM_DELIVERY NUMBER DEFAULT 0 NOT NULL ,
	AVERAGE_POINT NUMBER DEFAULT 0 NOT NULL,
	LAST_LOGIN_TIME DATE,
	ACCOUNT_CREATION_DATE DATE DEFAULT SYSDATE NOT NULL,
	LEAVE_DATE DATE,
    CONSTRAINT STORE_STORE_ID_PK PRIMARY KEY(STORE_ID),
    CONSTRAINT SOTRE_STORE_NAME_UQ UNIQUE(STORE_NAME),
    CONSTRAINT STORE_SNSID_SNSTYPE_UQ UNIQUE(SNSID,SNSTYPE),
    CONSTRAINT STORE_MOBILE_OPEN_YN_CK CHECK(MOBILE_OPEN_YN IN ('Y', 'N')),
    CONSTRAINT STORE_AUTHENTICATION_YN_CK CHECK(AUTHENTICATION_YN IN ('Y', 'N'))
);

--COMMENT 등록
