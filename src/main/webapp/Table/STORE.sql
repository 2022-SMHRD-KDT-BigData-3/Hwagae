COMMENT ON COLUMN STORE.STORE_NAME IS '상점명';
COMMENT ON COLUMN STORE.STORE_INFO IS '상점소개글';
COMMENT ON COLUMN STORE.SNSID IS '간편로그인ID';
COMMENT ON COLUMN STORE.SNSTYPE IS '간편로그인플랫폼';
COMMENT ON COLUMN STORE.MOBILE IS '연락처';
COMMENT ON COLUMN STORE.MOBILE_OPEN_YN IS '연락처공개여부';
COMMENT ON COLUMN STORE.CONTACT_START_TIME IS '연락가능시작시간';
COMMENT ON COLUMN STORE.CONTACT_END_TIME IS '연락가능종료시간';
COMMENT ON COLUMN STORE.AUTHENTICATION_MOBILE IS '본인인증연락처';
COMMENT ON COLUMN STORE.AUTHENTICATION_YN IS '본인인증여부';
COMMENT ON COLUMN STORE.AREA IS '지역';
COMMENT ON COLUMN STORE.ZIP_CODE IS '우편번호';
COMMENT ON COLUMN STORE.BASIC_ADDRESS IS '기본주소';
COMMENT ON COLUMN STORE.DTAIL_ADDRESS IS '상세주소';
COMMENT ON COLUMN STORE.NUM_VISITORS IS '방문자수';
