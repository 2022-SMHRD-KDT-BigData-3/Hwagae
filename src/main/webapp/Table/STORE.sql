--인덱스 생성
CREATE INDEX STORE_SNSTYPE_INDEX ON STORE(SNSTYPE);
CREATE INDEX STORE_AREA_INDEX ON STORE(AREA);
CREATE INDEX STORE_ZIP_CODE_INDEX ON STORE(ZIP_CODE);

--시퀀스 생성
CREATE SEQUENCE STORE_SEQ INCREMENT BY 1 START WITH 10000000;